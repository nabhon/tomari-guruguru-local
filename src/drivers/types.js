// ドライバ契約（型ドキュメントのみ・実行コードなし）
//
// キャラクターの挙動は 3 つの「チャンネル」に分かれ、それぞれを差し替え可能な
// 「ソース」が駆動する。レンダラ（talk-app.jsx）はチャンネルの値を
// 読むだけで、入力元が何かは知らない。
//
//   direction : マウス / 顔トラッキング → targetRef.current = {x,y} (-1..1)
//   mouth     : 音声 / 顔トラッキング     → 0|1|2 (とじ/中間/開け)
//   blink     : タイマー / 顔トラッキング  → boolean
//
// ── DirectionSource ──────────────────────────────────────────────
// 副作用フック。targetRef.current = {x, y} を更新するだけで戻り値はない。
//   useXxxDirection({ charRef, tweaksRef, targetRef }) -> void
// 平滑化とグリッド写像は useAvatarLoop 側が共通で担うので、ソースは生の
// ターゲット座標を書くことだけに責任を持つ。
//
// ── MouthSource ──────────────────────────────────────────────────
// メインループの onFrame から毎フレーム呼ばれ、新しい口段階を返す。
//   frame(now, tw) -> (0|1|2)|null     // 変化なしは null
// メータ表示などの副作用はソース内部で行う。
//
// ── BlinkSource ──────────────────────────────────────────────────
// enabled を受け取り blink 状態を返すフック。
//   useXxxBlink(enabled) -> boolean
//
// ── 複数チャンネルを束ねるソース（Cycle 3 の FaceSource 想定）─────
// 顔トラッキングは 1 回の推論パスから direction・mouth・blink を同時に得る。
// その場合は内部で 1 本の映像/推論ループを共有しつつ、上記 3 契約に対応する
// 薄いアダプタ（チャンネルごとのフック）を公開すればよい。チャンネル分割が
// カメラの多重読み取りを強制しない設計。
